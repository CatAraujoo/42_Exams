#include <stdio.h>

char    *ft_strrev(char *str)
{
	int	i = 0;
	int len = 0;
	char temp;

	while (str[len] != '\0')
		len++;
	len--;
	while (i < len)
	{
		temp = str[i];
		str[i] = str[len];
		str[len] = temp;
		i++;
		len--;
	}
	return (str);
}

int	main(void)
{
	char str[] = "Hello";

	printf ("%s", ft_strrev(str));
}

